using System;
using System.ComponentModel;
using System.Reflection;
//=====================================================================================================================
namespace Lotus
{
	namespace Core
	{
		//-------------------------------------------------------------------------------------------------------------
		//! \addtogroup CoreInspector
		/*@{*/
		//-------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Атрибут для определения редактора для свойства
		/// </summary>
		//-------------------------------------------------------------------------------------------------------------
		[AttributeUsage(AttributeTargets.Property, Inherited = false, AllowMultiple = false)]
		public class LotusInspectorTypeEditor : Attribute
		{
			#region ======================================= ДАННЫЕ ====================================================
			protected internal Type mEditorType;
			#endregion

			#region ======================================= СВОЙСТВА ==================================================
			/// <summary>
			/// Тип редактора для свойства
			/// </summary>
			public Type EditorType
			{
				get { return mEditorType; }
			}
			#endregion

			#region ======================================= КОНСТРУКТОРЫ ==============================================
			//---------------------------------------------------------------------------------------------------------
			/// <summary>
			/// Конструктор инициализирует объект класса указанными параметрами
			/// </summary>
			/// <param name="editor_type">Тип редактора для свойства</param>
			//---------------------------------------------------------------------------------------------------------
			public LotusInspectorTypeEditor(Type editor_type)
			{
				mEditorType = editor_type;
			}
			#endregion
		}
		//-------------------------------------------------------------------------------------------------------------
		/*@}*/
		//-------------------------------------------------------------------------------------------------------------
	}
}
