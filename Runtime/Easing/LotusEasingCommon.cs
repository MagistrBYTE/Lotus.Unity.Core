*		Определение функций плавности изменения скорости для создания более реалистичных анимации.
*		Реальные вещи не начинают двигаться мгновенно и с постоянной скоростью. Если мы открываем ящик стола, то сначала
*	ускоряем его, а на второй половине пути — тормозим. Если что-то упало, то оно сначала летит все быстрее и быстрее, 
*	а ударившись о пол — слегка подпрыгивает обратно. В каждом конкретном случаем нужно подбирать свою функцию плавности.
*/
