using System;
using System.Runtime.InteropServices;
using System.Collections.Generic;
using System.IO;
using System.Xml;
//=====================================================================================================================
namespace Lotus
{
	namespace Core
	{
		//-------------------------------------------------------------------------------------------------------------
		//! \defgroup CoreEasing Подсистема функций плавности
		//! Определение функций плавности изменения скорости для создания более реалистичных анимации.
		//! \ingroup Core
		/*@{*/
		//-------------------------------------------------------------------------------------------------------------
		/// <summary>
		/// Основные типы функции плавности
		/// </summary>
		/// <remarks>
		/// Под функцией понимается математическая закономерность изменения скорости от времени
		/// </remarks>
		//-------------------------------------------------------------------------------------------------------------
		public enum TEasingType
		{
			/// <summary>
			/// Линейная функция
			/// </summary>
			Linear,

			/// <summary>
			/// Квадратическая зависимость в начале
			/// </summary>
			QuadIn,

			/// <summary>
			/// Квадратическая зависимость в конце
			/// </summary>
			QuadOut,

			/// <summary>
			/// Квадратическая зависимость в начале и в конце
			/// </summary>
			QuadInOut,

			/// <summary>
			/// Кубическая зависимость в начале
			/// </summary>
			CubeIn,

			/// <summary>
			/// Кубическая зависимость в конце
			/// </summary>
			CubeOut,

			/// <summary>
			/// Кубическая зависимость в начале и в конце
			/// </summary>
			CubeInOut,

			/// <summary>
			/// Кратковременное изменение на противоположенное в начале
			/// </summary>
			BackIn,

			/// <summary>
			/// Кратковременное изменение на противоположенное в конце
			/// </summary>
			BackOut,

			/// <summary>
			/// Кратковременное изменение на противоположенное в начале и в конце
			/// </summary>
			BackInOut,

			/// <summary>
			/// Экспоненциальная зависимость в начале
			/// </summary>
			ExpoIn,

			/// <summary>
			/// Экспоненциальная зависимость в конце
			/// </summary>
			ExpoOut,

			/// <summary>
			/// Экспоненциальная зависимость в начале и в конце
			/// </summary>
			ExpoInOut,

			/// <summary>
			/// Синусоидальная зависимость в начале
			/// </summary>
			SineIn,

			/// <summary>
			/// Синусоидальная зависимость в конце
			/// </summary>
			SineOut,

			/// <summary>
			/// Синусоидальная зависимость в начале и в конце
			/// </summary>
			SineInOut,

			/// <summary>
			/// Колебательная форма в начале
			/// </summary>
			ElasticIn,

			/// <summary>
			/// Колебательная форма в конце
			/// </summary>
			ElasticOut,

			/// <summary>
			/// Колебательная форма в начале и в конце
			/// </summary>
			ElasticInOut,

			/// <summary>
			/// Форма отскока в начале
			/// </summary>
			BounceIn,

			/// <summary>
			/// Форма отскока в конце
			/// </summary>
			BounceOut,

			/// <summary>
			/// Форма отскока в начале и в конце
			/// </summary>
			BounceInOut
		}
		//-------------------------------------------------------------------------------------------------------------
		/*@}*/
		//-------------------------------------------------------------------------------------------------------------
	}
}
